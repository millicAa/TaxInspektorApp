    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed

        ModelTabele mt = (ModelTabele) tabela.getModel();
        ArrayList<StavkaPoreskePrijave> lista = mt.getListaStavki();
        if (txtGodina.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti godinu");
            return;
        }
        int godina = Integer.parseInt(txtGodina.getText());

        PoreskiObveznik po = (PoreskiObveznik) cmbPoreskiObveznik.getSelectedItem();

        if (lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "lista mora imati bar 1 stavku!");
            return;
        }

        PoreskaPrijava p = new PoreskaPrijava(-1, godina, po, pi, lista);
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(operacije.Operacije.UNESIPRIJAVU);
        kz.setP(p);
        KomunikacijaSaServerom.getKom().posaljiZahtev(kz);
        
       ServerskiOdgovor so = KomunikacijaSaServerom.getKom().prihvatiOdgovor();
       boolean uneo =(boolean) so.getOdgovor();
       if(uneo){
           JOptionPane.showMessageDialog(this, "Uspesno uneta prijava");
       }else{
           JOptionPane.showMessageDialog(this, "Neuspesan unos prijave");
       }
        
    }//GEN-LAST:event_btnSacuvajActionPerformed

