    private void ucitajCB() {
        ArrayList<VrstaPoreza> listaVP = new ArrayList<>();
        ArrayList<PoreskiObveznik> po = new ArrayList<>();

        cmbPoreskiObveznik.removeAllItems();
        cmbVrstaPoreza.removeAllItems();

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(operacije.Operacije.VRATIVRSTEIOBVEZNIKE);
        KomunikacijaSaServerom.getKom().posaljiZahtev(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getKom().prihvatiOdgovor();

        listaVP = so.getListaVrsti();
        po = so.getListaObveznika();

        for (PoreskiObveznik p : po) {
            cmbPoreskiObveznik.addItem(p);

        }
        for (VrstaPoreza v : listaVP) {
            cmbVrstaPoreza.addItem(v);
        }

    }

